// Primitives in JavaScript /Value Types
// 1. String
let x = 'hi';
console.log(x);
console.log(typeof x);
x = 10;
console.log(x);
console.log(typeof x);  //---> Js is a dynamic language


// 2. Number
let y  = 1;
console.log(y);
console.log(typeof y);

//3. Float is alos considered as Number in JavaScript
let z = 1.5;
console.log(z);
console.log(typeof z);

// 4. Boolean
let a = true;
console.log(a);
console.log(typeof a);

// 5. Null
let b = null;
console.log(b);
console.log(typeof b); // This will return 'object' due to a historical bug in JavaScript

// 6. Undefined
let c;
console.log(c);
console.log(typeof c);


